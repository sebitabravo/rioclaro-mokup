type RechartsComponent = {
  defaultProps?: unknown;
};

const sanitizeDefaultProps = (component: unknown) => {
  if (
    component &&
    typeof component === 'function' &&
    Object.prototype.hasOwnProperty.call(component, 'defaultProps')
  ) {
    Reflect.deleteProperty(component as RechartsComponent, 'defaultProps');
  }
};

